				ORG 0000H
				LJMP START
				ORG 0030H
	START:      MOV R0,#00H				;0000 0000
	DISPLAY:    MOV P0,R0
				ACALL DELAY				;跳转到标号DELAY处执行
				INC R0
				CJNE R0,#0FFH, DISPLAY	;若（Rn）≠1111 1111，则DISPLAY,or DELAY
				AJMP START

	DELAY:      MOV R5,#02H				;0000 0010
	DEL1:       MOV R6,#0F0H			;1111 0000
	DEL2:       MOV R7,#0F0H			;1111 0000
	DEL3:       DJNZ R7, DEL3
				DJNZ R6, DEL2
				DJNZ R5, DEL1
				RET
				END